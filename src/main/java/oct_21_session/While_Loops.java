package oct_21_session;

public class While_Loops {

	public static void main(String[] args) {
		// while loops is mostly used when there is infinite kind of a situation
		
		int b = 0;
		int c = 1;
		
		while(b != c) {
			System.out.println("logic is correct"); 
			break;
		}
		b++;
		System.out.println(b);
		

	}

}
